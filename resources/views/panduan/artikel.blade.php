    <h6>A. Menambah Artikel</h6>
    <div class="mb-3 text-center">
      <img src="{{ asset('images/panduan/artikel-tambah.png') }}" class="img-thumbnail" alt="">
    </div>
    <p>
        Untuk menambah artikel pengguna mesti menekan tombol tambah pada halaman artikel ataupun menekan tombol tambah artikel pada menu diatas. Kemudian mengisi semua bagian yang ada sesuai dengan penjelasannya dan ketika sudah selesai maka menekan tombol simpan untuk menyimpan semua data yang telah diisi.
    </p>
    <p>
        Pengguna juga dapat langsung menambahkan penulis artikel  <span class="badge badge-pill bg-warning-400 ml-auto ml-md-0">1</span> dan kategori artikel  <span class="badge badge-pill bg-warning-400 ml-auto ml-md-0">2</span>dengan menekan tombol <button type="button" class="btn btn-light"><i class="icon-plus22"></i></button> apabila kategori atau penulis yang diinginkan tidak ada.
    </p>
    <p>
      Pada bagian isi artikel  <span class="badge badge-pill bg-warning-400 ml-auto ml-md-0">4</span>, pengguna bisa langsung menuliskan isi artikel dan melakukan formating, membuat tabel serta menyisipkan foto dan link video pada artikel.
    </p>

    <p>Berikut adalah rincian masing-masing bagian</p>

    <ol>
      <li><strong>Tombol Tambah Penulis</strong> untuk menambah penulis baru / penulis yang tidak ada di daftar penulis</li>
      <li><strong>Tombol Tambah Kategori</strong> untuk menambah kateogri baru / kateogri yang tidak ada di daftar kateogri</li>
      <li><strong>Gambar Utama</strong> untuk menambahkan foto yang akan menjadi foto utama ketika melihat artikel</li>
      <li><strong>Isi Artikel</strong> untuk menuliskan semua isi dari artikel, disini pengguna bisa memberikan foto, tabel, pengaturan huruf dan menyisipkan video seperti pada penggunaan aplikasi word processing (Microsoft Word, Google Docs dan Pages)</li>
    </ol>

